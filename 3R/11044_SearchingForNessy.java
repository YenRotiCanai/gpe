// ans = m/3 * n/3
import java.util.*;

class Main{
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		int cases = sc.nextInt();
		while(cases-->0){
			int m = sc.nextInt();
			int n = sc.nextInt();

			System.out.println((m/3) * (n/3));
		}
	}
}